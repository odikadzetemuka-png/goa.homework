# рЃљрЃ«рЃАрЃћрЃюрЃўрЃЌ рЃарЃљ рЃљрЃарЃўрЃА рЃАрЃўрЃљ

# рЃАрЃўрЃљ (List) Рђћ рЃљрЃарЃўрЃА рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃўрЃА рЃћрЃарЃЌрЃЮрЃЉрЃџрЃўрЃЮрЃЉрЃљ HTML-рЃерЃў, рЃарЃЮрЃЏрЃћрЃџрЃўрЃф рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃўрЃюрЃцрЃЮрЃарЃЏрЃљрЃфрЃўрЃўрЃА рЃЕрЃљрЃЏрЃЮрЃюрЃљрЃЌрЃЋрЃљрЃџрЃўрЃА рЃАрЃљрЃ«рЃўрЃЌ рЃњрЃљрЃЏрЃЮрЃАрЃљрЃбрЃљрЃюрЃљрЃЊ.
# HTML-рЃерЃў рЃљрЃарЃАрЃћрЃЉрЃЮрЃЉрЃА рЃАрЃљрЃЏрЃў рЃбрЃўрЃърЃўрЃА рЃАрЃўрЃљ:

# рЃЊрЃљрЃџрЃљрЃњрЃћрЃЉрЃБрЃџрЃў рЃАрЃўрЃљ (<ol>)

# рЃЊрЃљрЃБрЃџрЃљрЃњрЃћрЃЉрЃћрЃџрЃў рЃАрЃўрЃљ (<ul>)

