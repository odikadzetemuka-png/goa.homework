# ­ЪДЕ 3) рЃљрЃ«рЃАрЃћрЃюрЃў рЃАрЃљрЃЏрЃўрЃЋрЃћ рЃАрЃўрЃљ


# 3) рЃљрЃ«рЃАрЃћрЃюрЃў рЃАрЃљрЃЏрЃўрЃЋрЃћ рЃАрЃўрЃљ

# рЃЊрЃљрЃџрЃљрЃњрЃћрЃЉрЃБрЃџрЃў рЃАрЃўрЃљ (<ol>) Рђћ рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃўрЃА рЃЊрЃљрЃАрЃљрЃџрЃљрЃњрЃћрЃЉрЃџрЃљрЃЊ рЃЌрЃљрЃюрЃЏрЃўрЃЏрЃЊрЃћрЃЋрЃарЃЮрЃЉрЃўрЃЌ (рЃЏрЃљрЃњ. 1, 2, 3 рЃљрЃю A, B, C).

# рЃЊрЃљрЃБрЃџрЃљрЃњрЃћрЃЉрЃћрЃџрЃў рЃАрЃўрЃљ (<ul>) Рђћ рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃћрЃџрЃћрЃЏрЃћрЃюрЃбрЃћрЃЉрЃўрЃА рЃЕрЃљрЃЏрЃЮрЃАрЃљрЃЌрЃЋрЃџрЃћрЃџрЃљрЃЊ, рЃарЃЮрЃфрЃљ рЃЌрЃљрЃюрЃЏрЃўрЃЏрЃЊрЃћрЃЋрЃарЃЮрЃЉрЃљ рЃљрЃа рЃљрЃарЃўрЃА рЃЏрЃюрЃўрЃерЃЋрЃюрЃћрЃџрЃЮрЃЋрЃљрЃюрЃў.

# рЃљрЃдрЃгрЃћрЃарЃўрЃџрЃЮрЃЉрЃўрЃЌрЃў рЃАрЃўрЃљ (<dl>) Рђћ рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЉрЃљ рЃбрЃћрЃарЃЏрЃўрЃюрЃўрЃАрЃљ рЃЊрЃљ рЃЏрЃўрЃАрЃў рЃљрЃдрЃгрЃћрЃарЃўрЃА рЃњрЃљрЃЏрЃЮрЃАрЃљрЃЕрЃћрЃюрЃљрЃЊ (рЃЏрЃљрЃњрЃљрЃџрЃўрЃЌрЃљрЃЊ рЃџрЃћрЃЦрЃАрЃўрЃЎрЃЮрЃюрЃерЃў рЃљрЃю рЃўрЃюрЃцрЃЮрЃарЃЏрЃљрЃфрЃўрЃљрЃерЃў).




