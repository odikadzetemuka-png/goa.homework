#  'მწვანე' გამოიტანეთ 'წადი', თუ ფერი არის ყვითელი მაშინ გამოიტანე 'მოემზადე', სხვა შემთხვევაში, ანუ წითელზე 'გაჩერდი'




color = input("შეიყვანე შუქნიშანის ფერი (მწვანე, ყვითელი, წითელი): ").lower()

if color == "მწვანე":
    print("წადი")
elif color == "ყვითელი":
    print("მოემზადე")
elif color == "წითელი":
    print("გაჩერდი")
else:
    print("არასწორი ფერია")
