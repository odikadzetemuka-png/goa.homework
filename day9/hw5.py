#  დაწერეთ კოდი, სადაც პროდუქტები რისიც ყიდვა გვინდა, ჯამში ღირს 22.50 ლარი,
#  თუ გვაქვს ამაზე მეტი, მაშინ გამოვიტანოთ 'ხურდა: 22.50 - ფული რაც გვაქვს', თუ არ გვყოფნის მაშინ გამოვიტანოთ: 'გაკლია 22.50 - რაც გვაქვს'



total = 22.50
money = float(input("რამდენი ფული გაქვს? "))

if money >= total:
    print("ხურდა:", money - total, "ლარი")
else:
    print("გაკლია:", total - money, "ლარი")
